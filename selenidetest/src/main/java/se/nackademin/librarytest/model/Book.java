package se.nackademin.librarytest.model;

/**
 * @author testautomatisering
 */
public class Book {
    private Integer id;
    private String title;
    private String author;
    private String description;
    private String isbn;
    private String datePublished;
    private String numberOfPages;
    private String copiesAvailible;
    private String totalNumberOfCopiesAvailible;

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * @return the author
     */
    public String getAuthor() {
        return author;
    }

    /**
     * @param author the author to set
     */
    public void setAuthor(String author) {
        this.author = author;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the isbn
     */
    public String getIsbn() {
        return isbn;
    }

    /**
     * @param isbn the isbn to set
     */
    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    /**
     * @return the datePublished
     */
    public String getDatePublished() {
        return datePublished;
    }

    /**
     * @param datePublished the datePublished to set
     */
    public void setDatePublished(String datePublished) {
        this.datePublished = datePublished;
    }

    /**
     * @return the numberOfPages
     */
    public String getNumberOfPages() {
        return numberOfPages;
    }

    /**
     * @param numberOfPages the numberOfPages to set
     */
    public void setNumberOfPages(String numberOfPages) {
        this.numberOfPages = numberOfPages;
    }

    /**
     * @return the copiesAvailible
     */
    public String getCopiesAvailible() {
        return copiesAvailible;
    }

    /**
     * @param copiesAvailible the copiesAvailible to set
     */
    public void setCopiesAvailible(String copiesAvailible) {
        this.copiesAvailible = copiesAvailible;
    }

    /**
     * @return the totalNumberOfCopiesAvailible
     */
    public String getTotalNumberOfCopiesAvailible() {
        return totalNumberOfCopiesAvailible;
    }

    /**
     * @param totalNumberOfCopiesAvailible the totalNumberOfCpiesAvailible to set
     */
    public void setTotalNumberOfCopiesAvailible(String totalNumberOfCopiesAvailible) {
        this.totalNumberOfCopiesAvailible = totalNumberOfCopiesAvailible;
    }





}
